// Helper functions
export function getDishesByCategory(category: string): Dish[] {
  return menuData.filter((dish) => dish.category === category);
}

export function getBestsellers(): Dish[] {
  return menuData.filter((dish) => dish.isBestseller);
}

export function getVegDishes(): Dish[] {
  return menuData.filter((dish) => dish.isVeg);
}

export function getNonVegDishes(): Dish[] {
  return menuData.filter((dish) => !dish.isVeg);
}

export function getDishById(id: string): Dish | undefined {
  return menuData.find((dish) => dish.id === id);
}

export function getNewDishes(): Dish[] {
  return menuData.filter((dish) => dish.isNew);
}

export function getAllCategories(): string[] {
  const categories = new Set(menuData.map((dish) => dish.category));
  return Array.from(categories);
}

export function searchDishes(query: string): Dish[] {
  const lowerQuery = query.toLowerCase();
  return menuData.filter(
    (dish) =>
      dish.name.toLowerCase().includes(lowerQuery) ||
      dish.description.toLowerCase().includes(lowerQuery) ||
      dish.category.toLowerCase().includes(lowerQuery) ||
      (dish.story && dish.story.toLowerCase().includes(lowerQuery))
  );
}
